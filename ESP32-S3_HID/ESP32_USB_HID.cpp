// Adafruit_USBD_HID 3.7.4 defines tud_hid_set/get_report_cb as non-weak and
// routes ALL report types (OUTPUT + FEATURE) to our registered callback.
// No override needed — just register via setReportCallback().
void ESP32_HID_Init() {
    // Com USB Mode=TinyUSB + CDC On Boot=Enabled:
    // O core Arduino já inicializou o CDC (Serial) via Adafruit TinyUSB.
    // clearConfiguration() chamado dentro de Serial.begin() sobrescreveu as
    // strings com o macro USB_PRODUCT do board ("ESP32S3_DEV").
    // Redefinimos AQUI, depois adicionamos o HID e forçamos re-enumeração.

    // 1) Corrige strings ANTES de begin() adicionar o HID
    TinyUSBDevice.setManufacturerDescriptor("Jean-DrEaD");
    TinyUSBDevice.setProductDescriptor("ESP32-S3-FFB-Wheel");

    // 2) Adiciona HID ao dispositivo composto
    s_HID.setReportCallback(get_report_cb, set_report_cb);
    s_HID.begin();

    // 3) Força re-enumeração USB via API TinyUSB de baixo nível.
    //    Adafruit_USBD_Device 3.7.4 não expõe disconnect()/connect().
    //    tud_disconnect() / tud_connect() estão em tusb.h (já incluído).
    tud_disconnect();
    delay(50);
    tud_connect();

    // 4) Aguarda enumeração com HID (máx 3s)
    uint32_t t = millis();
    while (!TinyUSBDevice.mounted() && (millis()-t) < 3000) { delay(10); }
}

bool ESP32_HID_Ready() {
    return TinyUSBDevice.mounted();
}

void HID_SendReport(uint8_t id, const void* data, int len) {
    if (!s_HID.ready()) return;
    s_HID.sendReport(id, data, (uint8_t)len);
}

void USB_SendControl(uint8_t /*flags*/, const void* data, int len) {
    // ─── CRITICAL FIX ─────────────────────────────────────────────────────────
    // TinyUSB's get_report_cb is expected to fill `buffer` with PAYLOAD only —
    // it automatically prepends the report_id byte on the wire.
    // The structs (PIDPool, PIDBlockLoad, etc.) all start with a reportId byte.
    // If we copy the whole struct, TinyUSB adds a second report_id prefix →
    // malformed GET_FEATURE response → Windows reads garbage field values:
    //   PIDPool:      ramPoolSize gets corrupted  → driver can't set up memory
    //   PIDBlockLoad: effectBlockIndex = reportId → block allocation fails
    // Fix: skip the first byte (reportId) before storing in g_featureReportBuf.
    // ──────────────────────────────────────────────────────────────────────────
    if (len <= 1) return;
    const uint8_t* p = (const uint8_t*)data + 1;  // skip reportId byte
    int actual = len - 1;
    uint16_t n = (actual < (int)sizeof(g_featureReportBuf))
                     ? (uint16_t)actual : (uint16_t)sizeof(g_featureReportBuf);
    memcpy(g_featureReportBuf, p, n);
    g_featureReportLen = n;
}

int USB_RecvControl(void* data, int len) {
    // FIX: data is already in s_featureBuf (copied by set_report_cb before
    // HID_Setup_Callback fires). Just copy it out — no waiting needed.
    if (!s_recvReady) {
        // Fallback wait in case called outside of set_report_cb context
        uint32_t t = millis();
        while (!s_recvReady && (millis() - t) < 5) { yield(); }
    }
    if (!s_recvReady) return 0;
    int n = (len < s_featureLen) ? len : s_featureLen;
    if (n > 0) memcpy(data, s_featureBuf, (size_t)n);
    s_recvReady  = false;
    s_featureLen = 0;
    return n;
}

