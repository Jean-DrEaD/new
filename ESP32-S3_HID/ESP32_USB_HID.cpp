//
// Assinaturas EXATAS de Adafruit_USBD_HID.h 3.7.4 (verificado pelo erro do compilador):
//   get_report_callback_t = uint16_t (*)(uint8_t report_id, hid_report_type_t, uint8_t*, uint16_t)
//   set_report_callback_t = void     (*)(uint8_t report_id, hid_report_type_t, const uint8_t*, uint16_t)
//
// ATENÇÃO: o primeiro parâmetro É o report_id, não 'instance'.
// O TinyUSB REMOVE o byte de report_id do buffer antes de chamar o callback —
// portanto buffer[0] é o PRIMEIRO byte do payload, não o report_id.
// ─────────────────────────────────────────────────────────────────────────────

// GET_FEATURE: device → PC (PID Block Load report_id=6, PID Pool report_id=7)
// Chamado quando Windows faz GET_REPORT para um feature report.
static uint16_t get_report_cb(uint8_t report_id,
                               hid_report_type_t type,
                               uint8_t* buffer, uint16_t reqlen) {
    if (USBDevice.HID_Setup_Callback) {
        g_featureReportLen = 0;  // clear stale data BEFORE callback fills it
        Setup s;
        s.bmRequestType = REQUEST_DEVICETOHOST_CLASS_INTERFACE;
        s.bRequest      = HID_GET_REPORT;
        s.wValueL       = report_id;
        s.wValueH       = (uint8_t)type;
        s.wIndex        = 0;
        s.wLength       = reqlen;
        USBDevice.HID_Setup_Callback(s);  // ffb.ino::HID_GetReport fills g_featureReportBuf
    }
    uint16_t n = (g_featureReportLen < reqlen) ? g_featureReportLen : reqlen;
    if (n) {
        memcpy(buffer, g_featureReportBuf, n);
        g_featureReportLen = 0;
        // Debug: confirm payload (WITHOUT reportId) sent to TinyUSB
        Serial.printf("[USB] GET_FEATURE id=0x%02X payload_len=%u buf[0]=0x%02X buf[1]=0x%02X\n",
                      report_id, n,
                      n > 0 ? buffer[0] : 0xFF,
                      n > 1 ? buffer[1] : 0xFF);
    }
    return n;
}

// SET_REPORT: OUTPUT reports (FFB commands) + SET_FEATURE (Create New Effect etc.)
// O TinyUSB remove o report_id do buffer — está separado no parâmetro report_id.
static void set_report_cb(uint8_t report_id,
                           hid_report_type_t type,
                           uint8_t const* buffer, uint16_t bufsize) {
    if (type == HID_REPORT_TYPE_OUTPUT) {
        // ── OUTPUT: FFB command → FfbOnUsbData() ──────────────────────────────
        // FfbOnUsbData() espera data[0]=report_id, mas TinyUSB já removeu o byte.
        // Reconstitui: [report_id, payload...]
        if (!USBDevice.HID_ReceiveReport_Callback) return;
        uint8_t buf[65];
        buf[0] = report_id;
        uint16_t n = (bufsize > 64) ? 64 : bufsize;
        memcpy(buf + 1, buffer, n);
        USBDevice.HID_ReceiveReport_Callback(buf, n + 1);

    } else {
        // ── SET_FEATURE: Create New Effect (report_id=5/0x15) etc. ────────────
        // FIX: prepend report_id then payload, matching what USB_RecvControl
        // callers expect (struct starts with reportId byte).
        // TinyUSB strips report_id from buffer, so we add it back manually.
        {
            s_featureBuf[0] = report_id;  // restore stripped report_id
            uint16_t n = (bufsize > 63) ? 63 : (uint16_t)bufsize;
            memcpy(s_featureBuf + 1, buffer, n);
            s_featureLen = (int)(n + 1);
            s_recvReady  = true;  // data is ready BEFORE callback fires
            Serial.printf("[USB] SET_FEATURE id=0x%02X len=%u buf[0]=0x%02X\n",
                          report_id, n, buffer[0]);
        }
        if (USBDevice.HID_Setup_Callback) {
            Setup s;
            s.bmRequestType = REQUEST_HOSTTODEVICE_CLASS_INTERFACE;
            s.bRequest      = HID_SET_REPORT;
            s.wValueL       = report_id;
            s.wValueH       = (uint8_t)type;
            s.wIndex        = 0;
            s.wLength       = bufsize;
            USBDevice.HID_Setup_Callback(s);
        }
    }
}

