    j[5]=(uint8_t)(((z>>8)&0xF)|((rx&0xF)<<4));
    j[6]=(uint8_t)(rx>>4);
    j[7]=(uint8_t)ry;
    // PACKING ORIGINAL — obrigatório para compatibilidade com Wheel Control:
    // j[8][3:0] = RY high nibble
    // j[8][7:4] = buttons bits[3:0] → campo HAT no descriptor
    //             Com BUTTON0-3 mapeados ao pino 48 (sempre HIGH=nao pressionado),
    //             bits 0-3 ficam em 0 → HAT = 0 = sem direcao
